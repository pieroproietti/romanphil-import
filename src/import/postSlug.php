function postSlug($var)
{
    $var = str_replace('*', '', $var);
    $var = str_replace('+', '', $var);
    $var = str_replace('.', '', $var);
    $var = str_replace(',', '', $var);
    $var = str_replace(';', '', $var);
    $var = str_replace(':', '', $var);
    $var = str_replace('?', '', $var);
    $var = str_replace('!', '', $var);
    $var = str_replace("'", '', $var);
    $var = str_replace('"', '', $var);
    $var = str_replace('°', '', $var);
    $var = str_replace('/', '-', $var);
    $var = str_replace('\\', '-', $var);
    $var = str_replace('&', '-', $var);
    $var= strtolower(preg_replace(array('/[^a-zA-Z0-9 -]/', '/[ -]+/', '/^-|-$/'), array('', '-', ''), removeAccent($var)));
    /*
    * Rimouve il tratino se finale
    */
    $len=strlen($var);
    $righMinus=strrpos($var, "-");
    if ($righMinus==$len-1) {
        $var=substr($var, 0, $righMinus);
    }
    return $var;
}

function spacesRemove($var)
{
    $var=preg_replace(array('/\s{2,}/', '/[\t\n]/'), ' ', $var);
    return $var;
}

